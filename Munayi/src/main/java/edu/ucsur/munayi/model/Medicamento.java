package edu.ucsur.munayi.model;

public class Medicamento {
	private String nombre;

	public Medicamento() {
		super();
	}

	public Medicamento(String nombre) {
		super();
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
